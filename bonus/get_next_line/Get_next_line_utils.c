#include "../Bonus.h"

int	search_nl(char *st)
{
	int	i;

	i = 0;
	if (!st)
		return (-1);
	while (st[i])
	{
		if (st[i] == '\n')
			return (i + 1);
		i++;
	}
	return (-1);
}
